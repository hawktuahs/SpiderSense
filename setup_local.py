def main():
    """Main setup function"""
    print("ğŸŒŸ SmartSense Local Setup")
    print("=" * 50)
    
    # Change to project directory
    script_dir = Path(__file__).parent
    os.chdir(script_dir)
    
    # Check requirements
    if not check_requirements():
        print("\nâŒ Requirements check failed")
        sys.exit(1)
    
    # Setup environments
    success = True
    success &= setup_python_env()
    success &= setup_node_env()
    success &= setup_env_file()
